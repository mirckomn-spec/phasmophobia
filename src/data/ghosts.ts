export function getGhostById(id: string): Ghost | undefined {
  return GHOSTS.find((g) => g.id === id);
}

export function searchGhosts(query: string, evidenceFilter: string[]): Ghost[] {
  const q = query.toLowerCase().trim();
  return GHOSTS.filter((ghost) => {
    const matchesQuery =
      !q ||
      ghost.name.toLowerCase().includes(q) ||
      ghost.description.toLowerCase().includes(q) ||
      ghost.evidence.some((e) => e.toLowerCase().includes(q)) ||
      ghost.strengths.some((s) => s.toLowerCase().includes(q));

    const matchesEvidence =
      evidenceFilter.length === 0 ||
      evidenceFilter.every((ef) => ghost.evidence.includes(ef));

    return matchesQuery && matchesEvidence;
  });
}
